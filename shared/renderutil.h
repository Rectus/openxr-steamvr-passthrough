inline bool IsDepthFormat(DXGI_FORMAT in)
{
	switch (in)
	{
	case DXGI_FORMAT_D32_FLOAT:
	case DXGI_FORMAT_D32_FLOAT_S8X24_UINT:
	case DXGI_FORMAT_D24_UNORM_S8_UINT:
	case DXGI_FORMAT_D16_UNORM:
		return true;

	default:
		return false;
	}
}

inline DXGI_FORMAT DXGI_DepthFormatToTypeless(DXGI_FORMAT in)
{
	switch (in)
	{
	case DXGI_FORMAT_D32_FLOAT:
		return DXGI_FORMAT_R32_TYPELESS;
	case DXGI_FORMAT_D32_FLOAT_S8X24_UINT:
		return DXGI_FORMAT_R32_FLOAT_X8X24_TYPELESS;
	case DXGI_FORMAT_D24_UNORM_S8_UINT:
		return DXGI_FORMAT_R24G8_TYPELESS;
	case DXGI_FORMAT_D16_UNORM:
		return DXGI_FORMAT_R16_TYPELESS;

	default:
		return DXGI_FORMAT_UNKNOWN;
	}
}

inline DXGI_FORMAT VulkanImageFormatToDXGI(VkFormat in)
{
	switch (in)
	{
	case VK_FORMAT_R8G8B8A8_SRGB:
		return DXGI_FORMAT_R8G8B8A8_UNORM_SRGB;
	case VK_FORMAT_R8G8B8A8_UNORM:
		return DXGI_FORMAT_R8G8B8A8_UNORM;
	case VK_FORMAT_B8G8R8A8_SRGB:
		return DXGI_FORMAT_B8G8R8A8_UNORM_SRGB;
	case VK_FORMAT_R32G32B32A32_SFLOAT:
		return DXGI_FORMAT_R32G32B32A32_FLOAT;
	case VK_FORMAT_R32G32B32_SFLOAT:
		return DXGI_FORMAT_R32G32B32_FLOAT;
	case VK_FORMAT_R16G16B16A16_SFLOAT:
		return DXGI_FORMAT_R16G16B16A16_FLOAT;
	case VK_FORMAT_R16G16B16A16_UNORM:
		return DXGI_FORMAT_R16G16B16A16_UNORM;
	case VK_FORMAT_A2B10G10R10_UNORM_PACK32:
		return DXGI_FORMAT_R10G10B10A2_UNORM;
	case VK_FORMAT_D32_SFLOAT:
		return DXGI_FORMAT_D32_FLOAT;
	case VK_FORMAT_D32_SFLOAT_S8_UINT:
		return DXGI_FORMAT_D32_FLOAT_S8X24_UINT;
	case VK_FORMAT_D24_UNORM_S8_UINT:
		return DXGI_FORMAT_D24_UNORM_S8_UINT;
	case VK_FORMAT_D16_UNORM:
		return DXGI_FORMAT_D16_UNORM;
	case VK_FORMAT_R16G16_SNORM:
		return DXGI_FORMAT_R16G16_SNORM;
	case VK_FORMAT_R16G16_SFLOAT:
		return DXGI_FORMAT_R16G16_FLOAT;

	case VK_FORMAT_UNDEFINED:
		return DXGI_FORMAT_UNKNOWN;

	default:
		g_logger->error("Unhandled Vulkan image format {}", static_cast<int32_t>(in));
		return DXGI_FORMAT_UNKNOWN;
	}
}

inline DXGI_FORMAT OpenGLImageFormatToDXGI(int64_t in)
{
	switch (in)
	{
	case GL_SRGB8_ALPHA8:
		return DXGI_FORMAT_R8G8B8A8_UNORM_SRGB;
		//case ?: There seems to be no way of specifying a BRGA foramt
		//	return DXGI_FORMAT_B8G8R8A8_UNORM_SRGB;
	case GL_RGBA8:
		return DXGI_FORMAT_R8G8B8A8_UNORM;
	case GL_RGBA8_SNORM:
		return DXGI_FORMAT_R8G8B8A8_SNORM;
	case GL_RGBA32F:
		return DXGI_FORMAT_R32G32B32A32_FLOAT;
	case GL_RGB32F:
		return DXGI_FORMAT_R32G32B32_FLOAT;
	case GL_RGBA16F:
		return DXGI_FORMAT_R16G16B16A16_FLOAT;
	case GL_RGBA16:
		return DXGI_FORMAT_R16G16B16A16_UNORM;
	case GL_RGB10_A2:
		return DXGI_FORMAT_R10G10B10A2_UNORM;
	case GL_DEPTH_COMPONENT32F:
	case GL_DEPTH_COMPONENT32: // Assuming float for 32 bit depth buffers
		return DXGI_FORMAT_D32_FLOAT;
	case GL_DEPTH32F_STENCIL8:
	case GL_DEPTH32F_STENCIL8_NV:
		return DXGI_FORMAT_D32_FLOAT_S8X24_UINT;
	case GL_DEPTH24_STENCIL8:
		return DXGI_FORMAT_D24_UNORM_S8_UINT;
	case GL_DEPTH_COMPONENT16:
		return DXGI_FORMAT_D16_UNORM;

	case GL_NONE:
		return DXGI_FORMAT_UNKNOWN;

		//case GL_RGB8:
		//case GL_DEPTH_COMPONENT24:

	default:
		g_logger->error("Unhandled OpenGL image format 0x{:x}", in);
		return DXGI_FORMAT_UNKNOWN;
	}
}
